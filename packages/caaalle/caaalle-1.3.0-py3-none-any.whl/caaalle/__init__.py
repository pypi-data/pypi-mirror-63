def add(initial: int=0, number: int=0) -> int:
    """Return sum of *intial* and *number*.
    :param initial: Initial value.
    :type initial: int
    :param number: Value to add to initial.
    :type number: int
    :return: Sum of initial and number.
    :rtype: int
    """
    return initial + number
